const recentEmojis = ["😀", "❤️", "😂", "🔥", "👍", "😍", "🎉", "💯", "😊", "🤔", "👋", "🙏"]

export function EmojiPicker({ onEmojiSelect, isOpen, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState("smileys")
  const [searchQuery, setSearchQuery] = useState("")
  const pickerRef = useRef<HTMLDivElement>(null)

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target as Node)) {
        onClose()
      }
    }

    if (isOpen) {
      document.addEventListener("mousedown", handleClickOutside)
    }

    return () => {
      document.removeEventListener("mousedown", handleClickOutside)
    }
  }, [isOpen, onClose])

  const filteredEmojis = searchQuery
    ? emojiCategories.flatMap((category) =>
        category.emojis.filter((emoji) =>
          // Simple search - could be enhanced with emoji names
          emoji.includes(searchQuery),
        ),
      )
    : emojiCategories.find((cat) => cat.id === activeCategory)?.emojis || []

  if (!isOpen) return null

  return (
    <div className="absolute bottom-full right-0 mb-2 z-50">
      <div
        ref={pickerRef}
        className="w-80 h-96 bg-background border border-border rounded-2xl shadow-2xl overflow-hidden animate-in slide-in-from-bottom-2 duration-200"
      >
        {/* Header */}
        <div className="p-3 border-b border-border">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 text-muted-foreground w-4 h-4" />
            <Input
              placeholder="Поиск эмодзи..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="pl-10 h-8 text-sm search-input"
            />
          </div>
        </div>

        {/* Recent Emojis */}
        {!searchQuery && (
          <div className="p-3 border-b border-border">
            <h3 className="text-xs font-medium text-muted-foreground mb-2">Недавние</h3>
            <div className="grid grid-cols-8 gap-1">
              {recentEmojis.map((emoji, index) => (
                <button
                  key={index}
                  onClick={() => onEmojiSelect(emoji)}
                  className="w-8 h-8 flex items-center justify-center text-lg hover:bg-muted rounded-md transition-colors"
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        )}

        {/* Category Tabs */}
        {!searchQuery && (
          <div className="flex border-b border-border bg-muted/30">
            {emojiCategories.map((category) => {
              const IconComponent = category.icon
              return (
                <button
                  key={category.id}
                  onClick={() => setActiveCategory(category.id)}
                  className={`flex-1 p-2 flex items-center justify-center transition-colors ${
                    activeCategory === category.id
                      ? "bg-primary text-primary-foreground"
                      : "hover:bg-muted text-muted-foreground"
                  }`}
                >
                  <IconComponent className="w-4 h-4" />
                </button>
              )
            })}
          </div>
        )}

        {/* Emoji Grid */}
        <ScrollArea className="flex-1 h-64">
          <div className="p-3">
            {searchQuery && (
              <h3 className="text-xs font-medium text-muted-foreground mb-2">
                Результаты поиска ({filteredEmojis.length})
              </h3>
            )}
            <div className="grid grid-cols-8 gap-1">
              {filteredEmojis.map((emoji, index) => (
                <button
                  key={index}
                  onClick={() => onEmojiSelect(emoji)}
                  className="w-8 h-8 flex items-center justify-center text-lg hover:bg-muted rounded-md transition-colors hover:scale-110 active:scale-95"
                >
                  {emoji}
                </button>
              ))}
            </div>
            {filteredEmojis.length === 0 && searchQuery && (
              <div className="text-center text-muted-foreground py-8">
                <Smile className="w-8 h-8 mx-auto mb-2 opacity-50" />
                <p className="text-sm">Эмодзи не найдены</p>
              </div>
            )}
          </div>
        </ScrollArea>
      </div>
    </div>
  )
}
